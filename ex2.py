# celcius to farenheit

Celcius = float(input("Enter temperature(in C): "))


# defining a function to covert celcius to farenheit.

def convert_to_farenheit(Celcius):
      Farenheit = ((9 * Celcius)/5) + 32
      print("Temperature in F:",Farenheit)


convert_to_farenheit(Celcius)


