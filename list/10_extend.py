# extend()

'''
This method adds an entire list or any 
other collection datatype (set, tuple, dictionary) 
to the existing list.

'''
fruits = ["apple", "banana", "cherry"]
print(fruits)

veg = ["carrot","tomato","cabbage"]
print(veg)

fruits.extend(veg) # adding "veg" to "fruits"
print(fruits)



