# Change List Items

'''
Changing items from list is easier, you just have to 
specify the index where you want to replace the item 
with existing item.

'''

colors = ['red','blue','green','orange','cyan']
print(colors) # ['red', 'blue', 'green', 'orange', 'cyan']

colors[2] = 'yellow'
print(colors) # ['red', 'blue', 'yellow', 'orange', 'cyan']