# Python Tuples

# Tuples are ordered collection of data items.
# They store multiple items in a single variable.
# Tuple items are separated by commas and enclosed within round brackets ().
# Tuples are immutable meaning we can not alter them after creation.

tuple1 = (5,6,8,9,1,2)
print(tuple1)

tuple2 = ("red","green","blue")
print(tuple2)

details = ("Dhyey",21,"SY B.tech","CSE")
print(details)

# NOTE : We cant perform sort operartions on tuples as it is immutable.
