# POSITIVE INDEXING

country = ("Spain", "Italy", "India", "England", "Germany")
#            [0]      [1]      [2]       [3]        [4]

print(country[2])
print(country[0])

print("\n")
# NEGATIVE INDEXING
fruits = ("Apple", "Banana", "Cherry", "Orange", "Kiwi")
#           [-5]     [-4]      [-3]      [-2]     [-1]
print(fruits[-1])
print(fruits[-4])