# Check for item
# We can check if a given item is present in the tuple. 
# This is done using the 'in' keyword.

fruits = ("Apple", "Banana", "Cherry", "Orange", "Kiwi")

if "Kiwi" in fruits:
      print(True)
else:
      print(False)


if "Avocado" in fruits:
      print(True)
else:
      print(False)