# Arithmetic Operators with datatypes
#            +   -   *   /   //  %   **
# int        Y   Y   Y   Y   Y   Y   Y
# float      Y   Y   Y   Y   Y   Y   Y
# bool       Y   Y   Y   Y   Y   Y   Y
# complex    Y   Y   Y   Y   N   Y   Y
# str        Y   N   Y   N   N   Y   N


# Int V/S
# int ----------> Y
# float --------> Y
# bool ---------> Y
# complex ------> Y (except //, %)
# str ----------> * only

# Float V/S
# int ----------> Y
# float --------> Y
# bool ---------> Y
# complex ------> Y (except //, %)
# str ----------> N

# Bool V/S
# int ----------> Y
# float --------> Y
# bool ---------> Y
# complex ------> Y (except //, %)
# str ----------> * only

# Complex V/S
# int ----------> Y (except //, %)
# float --------> Y (except //, %)
# bool ---------> Y (except //, %)
# complex ------> Y (except //, %)
# str ----------> N

# Str V/S
# int ----------> * only
# float --------> N
# bool ---------> * only
# complex ------> N
# str ----------> + only
