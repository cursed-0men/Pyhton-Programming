# BASE CONVERSION Functions
# 1. bin() == converts decimal to binary
# 2. oct() == converts decimal to octal
# 3. hex() == converts decimal to hexadecimal

x = 10
b = bin(x)
print(b)

print('\n')

y = 16
o = oct(y)
print(o)

print('\n')

z = 255
h = hex(z)
print(h)

d = True
t = bin(d)
print(t)
