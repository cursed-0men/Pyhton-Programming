# Logical operators
# used to write compound conditions.

# AND
# NOTE = AND is only true if both conditions are true simultaneously.
# Truth Table
#   A   B   AND
#   T   T    T
#   T   F    F
#   F   T    F
#   F   F    F

# OR
# NOTE = AND is true if atleast one of the conditions us true.
# Truth Table
#   A   B    OR
#   T   T    T
#   T   F    T
#   F   T    T
#   F   F    F

# NOT
# NOTE = True <---> False
# Truth Table
#   A    NOT
#   T     F
#   F     T

