# Loops
# 1. While Loops

# SYNTAX
# while condition:
#   statements(with indentation)

# These statements are called the while block.
# If the condition is true then while block gets executed till the condition becomes false and then it exits the loop.

# eg. Printing "hello" 10 times.
count = 0
while count < 10:
    print(count, "hello")  # here we are also printing count for our convenience.
    count += 1







